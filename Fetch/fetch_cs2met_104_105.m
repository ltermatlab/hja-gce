function [data,msg] = fetch_cs2met_104_105(template)

%connect to the database
conn = database('metdat', '', '', 'Vendor', 'MICROSOFT SQL SERVER', 'Server', 'Stewartia', 'PortNumber', 1433, 'AuthType', 'Windows');

if exist('template','var') ~= 1
   template = '';
end

qry = ['SELECT CS2MET_CLIM_105.TmStamp'...
    ' ,	CS2MET_CLIM_105.RecNum'...
    ' ,	CS2MET_CLIM_105.SITE_ID'...
    ' ,	CS2MET_CLIM_105.AIR_150_AVG'...
    ' ,	CS2MET_CLIM_105.RH_150_AVG'...
    ' ,	CS2MET_CLIM_105.DEWPT_150_AVG'...
    ' ,	CS2MET_CLIM_105.VAPDEF150_AVG'...
    ' ,	CS2MET_CLIM_105.AIR_150_MAX'...
    ' ,	CS2MET_CLIM_105.AIR_150_Hr_Min_MAX'...
    ' ,	CS2MET_CLIM_105.RH_150_MAX'...
    ' ,	CS2MET_CLIM_105.RH_150_Hr_Min_MAX'...
    ' ,	CS2MET_CLIM_105.DEWPT_150_MAX'...
    ' ,	CS2MET_CLIM_105.DEWPT_150_Hr_Min_MAX'...
    ' ,	CS2MET_CLIM_105.VAPDEF150_MAX'...
    ' ,	CS2MET_CLIM_105.VAPDEF150_Hr_Min_MAX'...
    ' ,	CS2MET_CLIM_105.AIR_150_MIN'...
    ' ,	CS2MET_CLIM_105.AIR_150_Hr_Min_MIN'...
    ' ,	CS2MET_CLIM_105.RH_150_MIN'...
    ' ,	CS2MET_CLIM_105.RH_150_Hr_Min_MIN'...
    ' ,	CS2MET_CLIM_105.DEWPT_150_MIN'...
    ' ,	CS2MET_CLIM_105.DEWPT_150_Hr_Min_MIN'...
    ' ,	CS2MET_CLIM_105.VAPDEF150_MIN'...
    ' ,	CS2MET_CLIM_105.VAPDEF150_Hr_Min_MIN'...
    '   FROM 	metdat.dbo.CS2MET_CLIM_105 '...
    '   WHERE SITE_ID = 104' ...
    '   AND year(TmStamp) = 2014'...
    '   ORDER BY TmStamp ASC '];

[data,msg] = sql2gceds(conn, ...
                        qry, ...
                        template, ...
                        1, ...
                        0, ...
                        1);

% %close database connection
 close(conn);



